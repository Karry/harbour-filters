#include "blurfilter.h"

#include <QColor>

BlurFilter::BlurFilter(QObject *parent) :
    AbstractImageFilter(parent)
{
}

QString BlurFilter::name() const
{
    return "Blur";
}

void BlurFilter::applyFilter(const QImage &origin)
{
    QImage newImage(origin.width(), origin.height(), QImage::Format_ARGB32);

    int kernel [5][5]= {{0,0,1,0,0},
                        {0,1,3,1,0},
                        {1,3,7,3,1},
                        {0,1,3,1,0},
                        {0,0,1,0,0}};
    int kernelSize = 5;
    int sumKernel = 27;
    int r,g,b;
    QColor color;

    for(int x=kernelSize/2; x<newImage.width()-(kernelSize/2); x++){
        for(int y=kernelSize/2; y<newImage.height()-(kernelSize/2); y++){

            r = 0;
            g = 0;
            b = 0;

            for(int i = -kernelSize/2; i<= kernelSize/2; i++){
                for(int j = -kernelSize/2; j<= kernelSize/2; j++){
                    color = QColor(origin.pixel(x+i, y+j));
                    r += color.red()*kernel[kernelSize/2+i][kernelSize/2+j];
                    g += color.green()*kernel[kernelSize/2+i][kernelSize/2+j];
                    b += color.blue()*kernel[kernelSize/2+i][kernelSize/2+j];
                }
            }

            r = qBound(0, r/sumKernel, 255);
            g = qBound(0, g/sumKernel, 255);
            b = qBound(0, b/sumKernel, 255);

            newImage.setPixel(x,y, qRgb(r,g,b));

        }
    }

    emit filterApplied(newImage);
}
